public class MyScoreAverage {
    public static void main(String[] args) {
        int Java=85,English=78,Math=92;
        double avg=(Java+English+Math)/3;
        
        System.out.println("Average score="+avg);
    }
}
/*
建立 MyScoreAverage.java，使用固定變數計算三科平均：

Java：85
English：78
Math：92
輸出：

Average score: 85.0
*/